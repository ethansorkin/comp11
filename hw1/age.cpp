#include <iostream>
using namespace std;
int main ()
{
  int age, hours;
  cout << "How old are you in years?" << endl;
  cin >> age;
  hours = age * (365 * 24);
  cout << "Then you are ";
  cout << hours;
  cout << " hours old!" << endl;
  return 0;
}

